package org.sadhar.sia.terpadu.cutinonakademis;

/**
 *
 * @author jasoet
 */
public class CutiNonAkademis {

    private String kodePegawai;
    private String namaPegawai;
    private String unitKerja;
    private int sisaCuti;

    public String getKodePegawai() {
        return kodePegawai;
    }

    public void setKodePegawai(String kodePegawai) {
        this.kodePegawai = kodePegawai;
    }

    public String getNamaPegawai() {
        return namaPegawai;
    }

    public void setNamaPegawai(String namaPegawai) {
        this.namaPegawai = namaPegawai;
    }

    public int getSisaCuti() {
        return sisaCuti;
    }

    public void setSisaCuti(int sisaCuti) {
        this.sisaCuti = sisaCuti;
    }

    public String getUnitKerja() {
        return unitKerja;
    }

    public void setUnitKerja(String unitKerja) {
        this.unitKerja = unitKerja;
    }
}
