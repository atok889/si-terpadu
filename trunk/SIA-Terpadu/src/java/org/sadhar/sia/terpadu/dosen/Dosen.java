package org.sadhar.sia.terpadu.dosen;

/**
 *
 * @author Hendro Steven
 */
public class Dosen {
    private String kdPegawai;
    private String nama;

    public Dosen(){}

    /**
     * @return the kdPegawai
     */
    public String getKdPegawai() {
        return kdPegawai;
    }

    /**
     * @param kdPegawai the kdPegawai to set
     */
    public void setKdPegawai(String kdPegawai) {
        this.kdPegawai = kdPegawai;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    
}
