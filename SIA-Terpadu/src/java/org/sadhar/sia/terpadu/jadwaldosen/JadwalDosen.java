package org.sadhar.sia.terpadu.jadwaldosen;

/**
 *
 * @author Hendro Steven
 */
public class JadwalDosen {
    private String jam;
    private String senin;
    private String selasa;
    private String rabu;
    private String kamis;
    private String jumat;
    private String sabtu;

    public JadwalDosen(){}

    /**
     * @return the jam
     */
    public String getJam() {
        return jam;
    }

    /**
     * @param jam the jam to set
     */
    public void setJam(String jam) {
        this.jam = jam;
    }

    /**
     * @return the senin
     */
    public String getSenin() {
        return senin;
    }

    /**
     * @param senin the senin to set
     */
    public void setSenin(String senin) {
        this.senin = senin;
    }

    /**
     * @return the selasa
     */
    public String getSelasa() {
        return selasa;
    }

    /**
     * @param selasa the selasa to set
     */
    public void setSelasa(String selasa) {
        this.selasa = selasa;
    }

    /**
     * @return the rabu
     */
    public String getRabu() {
        return rabu;
    }

    /**
     * @param rabu the rabu to set
     */
    public void setRabu(String rabu) {
        this.rabu = rabu;
    }

    /**
     * @return the kamis
     */
    public String getKamis() {
        return kamis;
    }

    /**
     * @param kamis the kamis to set
     */
    public void setKamis(String kamis) {
        this.kamis = kamis;
    }

    /**
     * @return the jumat
     */
    public String getJumat() {
        return jumat;
    }

    /**
     * @param jumat the jumat to set
     */
    public void setJumat(String jumat) {
        this.jumat = jumat;
    }

    /**
     * @return the sabtu
     */
    public String getSabtu() {
        return sabtu;
    }

    /**
     * @param sabtu the sabtu to set
     */
    public void setSabtu(String sabtu) {
        this.sabtu = sabtu;
    }

    
}
