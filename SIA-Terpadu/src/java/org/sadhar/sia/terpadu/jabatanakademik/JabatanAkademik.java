package org.sadhar.sia.terpadu.jabatanakademik;

/**
 *
 * @author jasoet
 */
public class JabatanAkademik {

    private String kode;
    private String nama;
    private String urut;

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getUrut() {
        return urut;
    }

    public void setUrut(String urut) {
        this.urut = urut;
    }
}
