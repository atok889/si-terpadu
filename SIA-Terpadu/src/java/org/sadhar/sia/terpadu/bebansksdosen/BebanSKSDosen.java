package org.sadhar.sia.terpadu.bebansksdosen;

/**
 *
 * @author Hendro Steven
 */
public class BebanSKSDosen {
    private String nama;
    private int sks;

    public BebanSKSDosen(){}

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the sks
     */
    public int getSks() {
        return sks;
    }

    /**
     * @param sks the sks to set
     */
    public void setSks(int sks) {
        this.sks = sks;
    }

    
}
